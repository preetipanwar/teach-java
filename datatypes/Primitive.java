class Primitive{
    public static void main(String[] args) {
    
        // popular primitive data types

        int     age     = 12;
        double  value   = 13.5;
        char    initial = 'A';
        boolean isNight = true;

        System.out.println(age);
        System.out.println(value);
        System.out.println(initial);
        System.out.println(isNight);

    } 
}